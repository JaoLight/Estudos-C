#include <stdio.h>

int main() {

    // Operadores Aritméticos

    /*
        Atribuição Simples (=)
        Atribuição com Soma (+=)
        Atribuição com Subtração (-=)
        Atribuição com Multiplicação (*=)
        Atribuição com Divisão (/=)
    */

    int num1, num2, resultado;

    resultado = 10;
    printf("Resultado: %d\n", resultado);

    resultado += 20;
    printf("Resultado: %d\n", resultado);

    resultado -= 5;
    printf("Resultado: %d\n", resultado);

    resultado *= 2;
    printf("Resultado: %d\n", resultado);

    resultado /= 5;
    printf("Resultado: %d\n", resultado);

    return 0;
}